
public class Chien {
	private String nomChien;
	private String couleurChien;
	public String getNomChien() {
		return nomChien;
	}
	public void setNomChien(String nomChien) {
		this.nomChien = nomChien;
	}
	public String getCouleurChien() {
		return couleurChien;
	}
	public void setCouleurChien(String couleurChien) {
		this.couleurChien = couleurChien;
	}
}
